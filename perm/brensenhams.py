import matplotlib.pyplot as plt

# Sample data - replace with your coordinates
x = [1, 2, 3, 4, 5]
y = [10, 12, 5, 8, 15]

# Create a scatter plot
plt.scatter(x, y, label='Data Points', color='b', marker='o')

# Add labels and a title
plt.xlabel('X-axis')
plt.ylabel('Y-axis')
plt.title('Scatter Plot Example')

# Add a legend
plt.legend()

# Display the plot
plt.show()




